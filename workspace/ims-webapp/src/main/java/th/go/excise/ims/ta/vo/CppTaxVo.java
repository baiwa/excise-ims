package th.go.excise.ims.ta.vo;

public class CppTaxVo {
	private Long id;
	private String list;
	private String quantity;
	private String priceRetail;
	private String cost;
	private String taxRate;
	private String taxAdditional;
	private String fine;
	private String extraMoney;
	private String taxLocal;
	private String total;

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getList() {
		return list;
	}

	public void setList(String list) {
		this.list = list;
	}

	public String getQuantity() {
		return quantity;
	}

	public void setQuantity(String quantity) {
		this.quantity = quantity;
	}

	public String getPriceRetail() {
		return priceRetail;
	}

	public void setPriceRetail(String priceRetail) {
		this.priceRetail = priceRetail;
	}

	public String getCost() {
		return cost;
	}

	public void setCost(String cost) {
		this.cost = cost;
	}

	public String getTaxRate() {
		return taxRate;
	}

	public void setTaxRate(String taxRate) {
		this.taxRate = taxRate;
	}

	public String getTaxAdditional() {
		return taxAdditional;
	}

	public void setTaxAdditional(String taxAdditional) {
		this.taxAdditional = taxAdditional;
	}

	public String getFine() {
		return fine;
	}

	public void setFine(String fine) {
		this.fine = fine;
	}

	public String getExtraMoney() {
		return extraMoney;
	}

	public void setExtraMoney(String extraMoney) {
		this.extraMoney = extraMoney;
	}

	public String getTaxLocal() {
		return taxLocal;
	}

	public void setTaxLocal(String taxLocal) {
		this.taxLocal = taxLocal;
	}

	public String getTotal() {
		return total;
	}

	public void setTotal(String total) {
		this.total = total;
	}

}
