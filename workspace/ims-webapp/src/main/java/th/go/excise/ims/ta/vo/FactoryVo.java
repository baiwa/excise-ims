package th.go.excise.ims.ta.vo;

public class FactoryVo {

	private String newRegId;
	private String cusFullname;
	private String facFullname;
	private String facAddress;
	private String secDesc;
	private String areaDesc;
	private String dutyDesc;
	private String auditType;
	private String auditStartDate;
	private String auditEndDate;

	public String getNewRegId() {
		return newRegId;
	}

	public void setNewRegId(String newRegId) {
		this.newRegId = newRegId;
	}

	public String getCusFullname() {
		return cusFullname;
	}

	public void setCusFullname(String cusFullname) {
		this.cusFullname = cusFullname;
	}

	public String getFacFullname() {
		return facFullname;
	}

	public void setFacFullname(String facFullname) {
		this.facFullname = facFullname;
	}

	public String getFacAddress() {
		return facAddress;
	}

	public void setFacAddress(String facAddress) {
		this.facAddress = facAddress;
	}

	public String getSecDesc() {
		return secDesc;
	}

	public void setSecDesc(String secDesc) {
		this.secDesc = secDesc;
	}

	public String getAreaDesc() {
		return areaDesc;
	}

	public void setAreaDesc(String areaDesc) {
		this.areaDesc = areaDesc;
	}

	public String getDutyDesc() {
		return dutyDesc;
	}

	public void setDutyDesc(String dutyDesc) {
		this.dutyDesc = dutyDesc;
	}

	public String getAuditType() {
		return auditType;
	}

	public void setAuditType(String auditType) {
		this.auditType = auditType;
	}

	public String getAuditStartDate() {
		return auditStartDate;
	}

	public void setAuditStartDate(String auditStartDate) {
		this.auditStartDate = auditStartDate;
	}

	public String getAuditEndDate() {
		return auditEndDate;
	}

	public void setAuditEndDate(String auditEndDate) {
		this.auditEndDate = auditEndDate;
	}

}
