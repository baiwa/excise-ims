package th.go.excise.ims.ta.vo;

public class CppFinishedGoodsReceiveVo {
	private Long id;
	private String list;
	private String finishedGoodsReceive;
	private String monthStatement;
	private String ps;
	private String maxDiff;

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getList() {
		return list;
	}

	public void setList(String list) {
		this.list = list;
	}

	public String getFinishedGoodsReceive() {
		return finishedGoodsReceive;
	}

	public void setFinishedGoodsReceive(String finishedGoodsReceive) {
		this.finishedGoodsReceive = finishedGoodsReceive;
	}

	public String getMonthStatement() {
		return monthStatement;
	}

	public void setMonthStatement(String monthStatement) {
		this.monthStatement = monthStatement;
	}

	public String getPs() {
		return ps;
	}

	public void setPs(String ps) {
		this.ps = ps;
	}

	public String getMaxDiff() {
		return maxDiff;
	}

	public void setMaxDiff(String maxDiff) {
		this.maxDiff = maxDiff;
	}

}
