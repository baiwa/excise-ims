package th.co.baiwa.excise.epa.persistence.vo;

public class Epa031Vo {

	private String exciseId;
	private String exciseName;
	private String destination;
	private String dateDestination;

	public String getExciseId() {
		return exciseId;
	}

	public String getExciseName() {
		return exciseName;
	}

	public String getDestination() {
		return destination;
	}

	public String getDateDestination() {
		return dateDestination;
	}

	public void setExciseId(String exciseId) {
		this.exciseId = exciseId;
	}

	public void setExciseName(String exciseName) {
		this.exciseName = exciseName;
	}

	public void setDestination(String destination) {
		this.destination = destination;
	}

	public void setDateDestination(String dateDestination) {
		this.dateDestination = dateDestination;
	}

}
